CREATE TABLE PUI_COINCIDENCIAS (
    ID                 NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_REPORTE         VARCHAR2(75)      NOT NULL,
    FASE_BUSQUEDA      VARCHAR2(1),
    TIPO_EVENTO        VARCHAR2(500),
    PAYLOAD_JSON       CLOB              NOT NULL,
    HTTP_STATUS        NUMBER(3),
    REQUEST_ID         VARCHAR2(64),
    ENDPOINT           VARCHAR2(120),
    FECHA_EVENTO       TIMESTAMP         DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_PUI_COINCIDENCIAS PRIMARY KEY (ID),
    CONSTRAINT CK_PUI_COINCIDENCIAS_FASE CHECK (FASE_BUSQUEDA IN ('1','2','3') OR FASE_BUSQUEDA IS NULL)
);

CREATE INDEX IDX_PUI_COINCIDENCIAS_REPORTE ON PUI_COINCIDENCIAS (ID_REPORTE);
CREATE INDEX IDX_PUI_COINCIDENCIAS_FASE ON PUI_COINCIDENCIAS (FASE_BUSQUEDA, FECHA_EVENTO);
