CREATE TABLE PUI_JOBS (
    ID                 NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    JOB_TYPE           VARCHAR2(40)      NOT NULL,
    ID_REPORTE         VARCHAR2(75)      NOT NULL,
    STATUS             VARCHAR2(20)      NOT NULL,
    RUN_AT             TIMESTAMP         NOT NULL,
    ATTEMPTS           NUMBER            DEFAULT 0 NOT NULL,
    MAX_ATTEMPTS       NUMBER            DEFAULT 10 NOT NULL,
    INTERVAL_MINUTES   NUMBER            DEFAULT 15 NOT NULL,
    LOCKED_BY          VARCHAR2(120),
    LOCKED_AT          TIMESTAMP,
    LAST_ERROR         VARCHAR2(4000),
    PAYLOAD_JSON       CLOB,
    UPDATED_AT         TIMESTAMP         DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_PUI_JOBS PRIMARY KEY (ID),
    CONSTRAINT UK_PUI_JOBS_TYPE_REPORTE UNIQUE (JOB_TYPE, ID_REPORTE),
    CONSTRAINT CK_PUI_JOBS_STATUS CHECK (STATUS IN ('PENDING','RUNNING','RETRY','FAILED','CANCELLED'))
);

CREATE INDEX IDX_PUI_JOBS_QUEUE ON PUI_JOBS (STATUS, RUN_AT);
